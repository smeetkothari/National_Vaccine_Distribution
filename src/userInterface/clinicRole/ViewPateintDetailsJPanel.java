    private void searchUsingInsuranceJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchUsingInsuranceJButtonActionPerformed
        // TODO add your handling code here:
        String patientInsuranceNumber = searchJTextField.getText();

        if (patientInsuranceNumber.equals("") || (patientInsuranceNumber.equals(null))) {
            JOptionPane.showMessageDialog(null, "Please enter a Insurance Number");

        } else {
            Patient patient = enterprise.searchUsingInsuranceNumber(patientInsuranceNumber);
            if (patient == null) {
                JOptionPane.showMessageDialog(null, "Please enter a Insurance Number");
                return;
            }
            SearchJPanel sjp = new SearchJPanel(userProcessContainer,enterprise,patient);
            userProcessContainer.add("SearchJPanel", sjp);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_searchUsingInsuranceJButtonActionPerformed

