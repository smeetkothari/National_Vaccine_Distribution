    private void populatePatientDetails() {
        DefaultTableModel dtm = (DefaultTableModel) patientDetailsJTable.getModel();
        dtm.setRowCount(0);

        for (Patient p : enterprise.getPatientList()) {
            Object row[] = new Object[5];
            row[0] = p;
            row[1] = p.getPatientName();
            row[2] = p.getDob();
            row[3] = p.getPatientAge();
            row[4] = p.getInsuranceStatus();
            dtm.addRow(row);
        }
    }

